"""
setup_wizard.py  —  Jarvis First-Run Setup
==========================================
Runs automatically the first time Jarvis starts (when .env is missing or empty).
Opens a clean GUI window that collects all required API keys and settings,
then writes a proper .env file and shows a confirmation before launching.

Usage (called automatically from main.py — see integration note at the bottom):
    from setup_wizard import run_if_needed
    run_if_needed()          # Must be called BEFORE any config / api imports
"""

