import json
import sys
import tkinter as tk
import tkinter.messagebox as messagebox

# Try to use customtkinter (same lib the rest of Jarvis uses); fall back to plain tk
try:
    import customtkinter as ctk
    USE_CTK = True
except ImportError:
    USE_CTK = False


